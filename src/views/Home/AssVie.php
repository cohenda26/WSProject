                    <h2 class="title m-t-20"> Contrats d’assurance vie </h2>
                    <p class="m-t-30"> L'assurance-vie est une forme d'assurance. 
                    La vocation d'origine des assurances-vie est de garantir le versement d'une certaine somme d'argent (capital ou rente) lorsque survient un événement lié à l'assuré : son décès ou sa survie. 
                    Il convient néanmoins de faire la distinction entre l'assurance en cas de décès dite « assurance décès » qui verse le capital ou la rente en cas de décès et l'assurance 
                    en cas de vie (aussi appelée assurance sur la vie), qui verse un capital ou une rente en cas de vie à échéance du contrat (si décès avant l'échéance rien n'est dû à la succession).
                    </p>
