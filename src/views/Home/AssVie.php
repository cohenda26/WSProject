                    <p>
                    Ce qui communément est appelé « assurance-vie » en France est un contrat d'assurance décès (capital versé en cas de décès avant le terme du contrat) avec « contre-assurance », 
                    c'est-à-dire le remboursement des primes versées pendant la durée du contrat en cas de vie de l'assuré au terme du contrat, et éventuellement majoré des intérêts techniques prévus au contrat. 
                    </p>
                    <p>
                    Ceci permet de présenter un quasi-produit d'épargne, doté des avantages fiscaux de l'assurance. Il est aussi possible de racheter son contrat avant l'échéance.
                    </p>
                    <p>L'assurance-vie permet aussi de faire fructifier des fonds tout en poursuivant un objectif à long terme : la retraite, un investissement immobilier, etc. Elle offre aussi d'importants avantages fiscaux en matière de succession.
                    </p>
                    <p>
                    Un contrat d'assurance-vie doit avoir une durée déterminée à la souscription, reconductible ou non selon les contrats par prorogation d'année en année. 
